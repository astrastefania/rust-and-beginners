#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn find_all_same_sum() {
        const DATA: [u32; 34] = [
            1, 4, 2, 3, 5, 1, 2, 3, 2, 1, 1, 1, 3, 2, 3, 2, 5, 3, 4, 2, 1, 2, 3, 1, 1, 2, 1, 5, 5,
            2, 3, 2, 2, 1,
        ];

        let out = all_same_sum_tuples(&DATA);
        let expected: Vec<(&[u32; WIN_SIZE], &[u32])> = vec![
            (&[1, 4, 2, 3, 5], &[2, 1, 1, 1, 3, 2, 3, 2]),
            (&[4, 2, 3, 5, 1], &[2, 1, 1, 1, 3, 2, 3, 2]),
            (&[2, 3, 5, 1, 2], &[2, 1, 2, 3, 1, 1, 2, 1]),
            (&[3, 5, 1, 2, 3], &[4, 2, 1, 2, 3, 1, 1]),
            (&[5, 1, 2, 3, 2], &[2, 1, 2, 3, 1, 1, 2, 1]),
            (&[1, 2, 3, 2, 1], &[2, 3, 1, 1, 2]),
            (&[2, 3, 2, 1, 1], &[2, 3, 1, 1, 2]),
            (&[3, 2, 1, 1, 1], &[3, 1, 1, 2, 1]),
            (&[2, 1, 1, 1, 3], &[3, 1, 1, 2, 1]),
            (&[1, 1, 1, 3, 2], &[3, 1, 1, 2, 1]),
            (&[1, 1, 3, 2, 3], &[2, 3, 1, 1, 2, 1]),
            (&[1, 3, 2, 3, 2], &[1, 2, 3, 1, 1, 2, 1]),
            (&[3, 2, 3, 2, 5], &[2, 3, 1, 1, 2, 1, 5]),
            (&[2, 3, 2, 5, 3], &[2, 3, 1, 1, 2, 1, 5]),
            (&[3, 2, 5, 3, 4], &[1, 1, 2, 1, 5, 5, 2]),
            (&[2, 5, 3, 4, 2], &[1, 2, 3, 1, 1, 2, 1, 5]),
            (&[5, 3, 4, 2, 1], &[2, 3, 1, 1, 2, 1, 5]),
            (&[3, 4, 2, 1, 2], &[5, 2, 3, 2]),
            (&[4, 2, 1, 2, 3], &[5, 2, 3, 2]),
            (&[2, 1, 2, 3, 1], &[2, 3, 2, 2]),
            (&[1, 2, 3, 1, 1], &[3, 2, 2, 1]),
            (&[2, 3, 1, 1, 2], &[2, 3, 2, 2]),
            (&[3, 1, 1, 2, 1], &[3, 2, 2, 1]),
            (&[1, 1, 2, 1, 5], &[2, 3, 2, 2, 1]),
        ];
        assert_eq!(out, expected);
    }
}

